    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(jtCodigo.getText());
        String descripcion = (String) jtDescripcion.getText();
        double precio = Double.parseDouble(jtPrecio.getText());
        int stock = Integer.parseInt(jtStock.getText());
        Categoria rubro = (Categoria) jcbRubro.getSelectedItem();

        Producto producto = new Producto(codigo, descripcion, precio, stock, rubro);

        Lista.listaProductos.add(producto);

        JOptionPane.showMessageDialog(this, "Producto guardado");
        jtCodigo.setText("");
        jtDescripcion.setText("");
        jtPrecio.setText("");
        jtStock.setText("");
        jcbRubro.setSelectedItem(-1);

    }//GEN-LAST:event_jbGuardarActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        // TODO add your handling code here:
        try {
            int elementoAEliminar = Integer.parseInt(jtCodigo.getText());
            int codigoAEliminar = Integer.parseInt(jtCodigo.getText());
            for (Producto producto : Lista.listaProductos) {
                if (producto.getCodigo() == elementoAEliminar) {
                    Lista.listaProductos.remove(producto);

                    JOptionPane.showMessageDialog(this, "El producto código: " + codigoAEliminar + " fue eliminado");
                    jtCodigo.setText("");
                    jtDescripcion.setText("");
                    jtPrecio.setText("");
                    jtStock.setText("");
                    jcbRubro.setSelectedItem(-1);
                }

            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El código debe ser válido");
        }


    }//GEN-LAST:event_jbEliminarActionPerformed

