    private void jbLupaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbLupaActionPerformed
        // TODO add your handling code here:
        int cont = 0;

        try {
            int codigoBuscado = Integer.parseInt(jtCodigo.getText());
            for (Producto producto : Lista.listaProductos) {

                if (codigoBuscado == producto.getCodigo()) {
                    cont++;
                    jtDescripcion.setText(producto.getDescripcion());
                    jtPrecio.setText(producto.getPrecio() + "");
                    jtStock.setText(producto.getStock() + "");
                    jcbRubro.setSelectedItem(producto.getRubro());
                }
            }
            if (cont == 0) {

                JOptionPane.showMessageDialog(this, "No hay productos con este codigo");

            }
        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(this, "Ingrese solo numeros");
            jtCodigo.setText("");
        }

    }//GEN-LAST:event_jbLupaActionPerformed

